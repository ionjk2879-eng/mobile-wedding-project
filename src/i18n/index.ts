const TRANSLATIONS: Record<SiteLang, Translations> = { ko, en, ja };

interface SiteLangContextType {
  lang: SiteLang;
  setLang: (l: SiteLang) => void;
  t: Translations;
}

const SiteLangContext = createContext<SiteLangContextType>({
  lang: 'ko',
  setLang: () => {},
  t: ko,
});

const STORAGE_KEY = 'sonett-site-lang';

export const SiteLangProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [lang, setLangState] = useState<SiteLang>(() => {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (saved === 'en' || saved === 'ja') return saved;
    return 'ko';
  });

  const setLang = (l: SiteLang) => {
    setLangState(l);
    localStorage.setItem(STORAGE_KEY, l);
  };

  useEffect(() => {
    document.documentElement.lang = lang;
  }, [lang]);

  return React.createElement(
    SiteLangContext.Provider,
    { value: { lang, setLang, t: TRANSLATIONS[lang] } },
    children,
  );
};

export const useSiteLang = () => useContext(SiteLangContext);