export function applyPreset(preset: AIPreset): InvitationData {
  const { settings } = preset;
  const merged: InvitationData = {
    ...initialData,
    ...settings,
    opening: {
      ...initialData.opening,
      ...(settings.opening || {}),
    },
  };
  if (settings.sectionOrder) {
    merged.templateSectionOrder = [...settings.sectionOrder];
  }
  return merged;
}
